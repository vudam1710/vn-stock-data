def add_rect(slide, x: float, y: float, w: float, h: float,
             fill_hex: str | None = None, line_hex: str | None = None,
             line_width: float = 0, corner_radius: int = 0):
    """Add a rectangle. fill_hex=None → transparent (no fill)."""
    shape = slide.shapes.add_shape(
        1,  # MSO_SHAPE_TYPE.RECTANGLE
        Inches(x), Inches(y), Inches(w), Inches(h)
    )
    # Large white/panel shapes → transparent fill + themed dashed border (matches reference design)
    is_panel = (fill_hex in ("#FFFFFF", C["panel_bg"])) and (w > 1.0 and h > 1.0)
    if is_panel:
        fill_hex = None  # transparent fill, delineated by border only

    if fill_hex is None:
        shape.fill.background()          # transparent
    else:
        shape.fill.solid()
        shape.fill.fore_color.rgb = rgb(fill_hex)

    if is_panel:
        _set_panel_border(shape)         # tx1 scheme color + sysDash
    elif line_hex and line_width > 0:
        shape.line.color.rgb = rgb(line_hex)
        shape.line.width = Pt(line_width)
        set_shape_dash_style(shape, "sysDot")
    else:
        shape.line.fill.background()     # no border

    # Unify all standard card panels to 1413 adjustments for a clean modern corner radius
    if 0 < corner_radius < 50000:
        corner_radius = 1413

    if corner_radius > 0:
        sp = shape._element
        spPr = sp.find(qn("p:spPr"))
        prstGeom = spPr.find(qn("a:prstGeom"))
        if prstGeom is None:
            custGeom = spPr.find(qn("a:custGeom"))
            if custGeom is not None:
                spPr.remove(custGeom)
            prstGeom = etree.SubElement(spPr, qn("a:prstGeom"))
            prstGeom.set("prst", "roundRect")
            avLst = etree.SubElement(prstGeom, qn("a:avLst"))
            gd = etree.SubElement(avLst, qn("a:gd"))
            gd.set("name", "adj")
            gd.set("fmla", f"val {corner_radius}")
        else:
            prstGeom.set("prst", "roundRect")
            avLst = prstGeom.find(qn("a:avLst"))
            if avLst is None:
                avLst = etree.SubElement(prstGeom, qn("a:avLst"))
            gd = avLst.find(qn("a:gd"))
            if gd is None:
                gd = etree.SubElement(avLst, qn("a:gd"))
            gd.set("name", "adj")
            gd.set("fmla", f"val {corner_radius}")
    return shape


def add_oval(slide, x: float, y: float, w: float, h: float, fill_hex: str):
    """Add a circle / oval shape."""
    from pptx.enum.shapes import MSO_SHAPE_TYPE
    shape = slide.shapes.add_shape(
        9,  # MSO_AUTO_SHAPE_TYPE.OVAL
        Inches(x), Inches(y), Inches(w), Inches(h)
    )
    shape.fill.solid()
    shape.fill.fore_color.rgb = rgb(fill_hex)
    shape.line.fill.background()
    return shape


def add_text(slide, text: str, x: float, y: float, w: float, h: float,
             font_name: str = "Calibri", font_size: float = 12,
             bold: bool = False, italic: bool = False,
             color_hex: str = "#111827", align=PP_ALIGN.LEFT,
             word_wrap: bool = True) -> object:
    """Add a plain text box."""
    txBox = slide.shapes.add_textbox(Inches(x), Inches(y), Inches(w), Inches(h))
    tf = txBox.text_frame
    tf.word_wrap = word_wrap
    p = tf.paragraphs[0]
    p.alignment = align
    run = p.add_run()
    run.text = str(text)
    run.font.name = font_name
    run.font.size = Pt(font_size)
    run.font.bold = bold
    run.font.italic = italic
    run.font.color.rgb = rgb(color_hex)
    return txBox


def add_rich_text(slide, segments: list, x: float, y: float, w: float, h: float,
                  font_name: str = "Calibri", font_size: float = 12,
                  word_wrap: bool = True) -> object:
    """
    segments = list of (text, bold, color_hex).
    All segments appear on the same paragraph (one text box, mixed formatting).
    """
    txBox = slide.shapes.add_textbox(Inches(x), Inches(y), Inches(w), Inches(h))
    tf = txBox.text_frame
    tf.word_wrap = word_wrap
    p = tf.paragraphs[0]
    p.alignment = PP_ALIGN.LEFT
    for (text, bold, color_hex) in segments:
        run = p.add_run()
        run.text = str(text)
        run.font.name = font_name
        run.font.size = Pt(font_size)
        run.font.bold = bold
        run.font.color.rgb = rgb(color_hex)
    return txBox


def add_image(slide, img_path: str | Path, x: float, y: float, w: float, h: float):
    """Add PNG image. If path not found, add gray placeholder rect."""
    img_path = Path(img_path)
    if img_path.exists():
        try:
            slide.shapes.add_picture(
                str(img_path),
                Inches(x), Inches(y), Inches(w), Inches(h)
            )
            return
        except Exception as e:
            _log.warning("image_embed_failed", img=str(img_path), error=str(e))
    # Placeholder
    add_rect(slide, x, y, w, h, fill_hex="#D1D5DB")
    add_text(slide, "[chart not found]", x + 0.1, y + h / 2 - 0.15, w - 0.2, 0.3,
             font_size=9, color_hex="#6B7280", align=PP_ALIGN.CENTER)


