def _estimate_cell_lines(text: str, col_width_inches: float, font_size_pt: float = 10) -> int:
    """Estimate number of wrapped lines for a cell given column width and font size.
    Uses average character width approximation: ~0.011" per pt per char (Calibri)."""
    if not text:
        return 1
    chars_per_line = max(1, int(col_width_inches / (font_size_pt * 0.011)))
    lines = 0
    for paragraph in text.split("\n"):
        words = paragraph.split()
        if not words:
            lines += 1
            continue
        current = 0
        for word in words:
            if current + len(word) + 1 > chars_per_line:
                lines += 1
                current = len(word)
            else:
                current += len(word) + 1
        lines += 1
    return max(1, lines)


def build_recs_table(prs, blank_layout, slide_data: dict):
    """RECS_TABLE — full-width recommendations table with dynamic row heights."""
    slide = add_slide(prs, blank_layout)
    _standard_header(slide, slide_data)

    content = slide_data.get("content", {})
    rows = content.get("rows", content.get("recommendations", slide_data.get("rows", [])))

    # Column widths (inches): PRIORITY | ACTION | OWNER | TIMELINE | EXPECTED IMPACT | SUCCESS METRIC
    col_widths  = [1.2,  4.0,    1.5,    1.2,      2.0,             1.66]
    col_headers = ["PRIORITY", "ACTION", "OWNER", "TIMELINE", "EXPECTED IMPACT", "SUCCESS METRIC"]
    font_size   = 10
    table_x     = 1.05
    header_y    = 1.76
    header_h    = 0.35
    line_h      = 0.175   # height per line at 10pt
    cell_pad_v  = 0.12    # top + bottom padding inside each cell
    min_row_h   = 0.42    # never shorter than this

    # Priority colour rules
    def priority_style(val: str) -> tuple[str | None, str, bool]:
        v = str(val).upper()
        if "P1-NOW" in v or "P1 NOW" in v:
            return C["red"], C["white"], True
        if "P1-30D" in v or "P1 30D" in v or "P1-30" in v:
            return C["orange"], C["white"], True
        return None, C["text_body"], False

    # Header row
    add_rect(slide, table_x, header_y, sum(col_widths), header_h, fill_hex=C["teal"])
    x_cur = table_x
    for hdr, cw in zip(col_headers, col_widths):
        add_text(slide, hdr, x_cur + 0.05, header_y + 0.07, cw - 0.10, header_h - 0.08,
                 font_name="Calibri", font_size=font_size, bold=True, color_hex=C["white"])
        x_cur += cw

    # Pre-compute dynamic height for every row
    def get_row_cells(row):
        return [
            row.get("priority",       row.get("Priority",       "")),
            row.get("action",         row.get("Action",         "")),
            row.get("owner",          row.get("Owner",          "")),
            row.get("timeline",       row.get("Timeline",       "")),
            row.get("expected_impact",row.get("impact",  row.get("Impact", ""))),
            row.get("success_metric", row.get("metric",  row.get("Metric", ""))),
        ]

    def compute_row_h(cells):
        usable_widths = [max(0.1, cw - 0.10) for cw in col_widths]
        max_lines = max(
            _estimate_cell_lines(str(v), uw, font_size)
            for v, uw in zip(cells, usable_widths)
        )
        return max(min_row_h, max_lines * line_h + cell_pad_v)

    # Body rows — dynamic height
    row_y = header_y + header_h
    for ri, row in enumerate(rows):
        cells = get_row_cells(row)
        row_h = compute_row_h(cells)
        row_bg = C["panel_bg"] if ri % 2 == 0 else C["content_bg"]
        add_rect(slide, table_x, row_y, sum(col_widths), row_h, fill_hex=row_bg)

        x_cur = table_x
        for ci, (cell_val, cw) in enumerate(zip(cells, col_widths)):
            cell_text = str(cell_val)

            if ci == 0:
                fill_col, text_col, is_bold = priority_style(cell_text)
                if fill_col:
                    add_rect(slide, x_cur + 0.03, row_y + 0.04,
                             cw - 0.06, row_h - 0.08, fill_hex=fill_col, corner_radius=10000)
                    add_text(slide, cell_text,
                             x_cur + 0.05, row_y + 0.07, cw - 0.10, row_h - 0.10,
                             font_name="Calibri", font_size=font_size, bold=is_bold,
                             color_hex=text_col, word_wrap=True)
                else:
                    add_text(slide, cell_text,
                             x_cur + 0.05, row_y + 0.07, cw - 0.10, row_h - 0.10,
                             font_name="Calibri", font_size=font_size, bold=False,
                             color_hex=C["text_body"], word_wrap=True)
            else:
                add_text(slide, cell_text,
                         x_cur + 0.05, row_y + 0.07, cw - 0.10, row_h - 0.10,
                         font_name="Calibri", font_size=font_size,
                         color_hex=C["text_body"], word_wrap=True)
            x_cur += cw

        row_y += row_h  # advance Y by actual row height

    return slide


