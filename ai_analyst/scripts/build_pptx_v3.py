import re

def clean_bold_separators(text: str) -> str:
    """
    Remove ':' or '—' immediately following markdown double asterisks '**' or similar bold boundaries.
    Example: '**Overall performance**: Q1...' -> '**Overall performance** Q1...'
             '**Overall performance** — Q1...' -> '**Overall performance** Q1...'
             '**Overall performance** : Q1...' -> '**Overall performance** Q1...'
    """
    if not text:
        return text
    # Match bold prefix and trailing horizontal spaces + separator + optional horizontal spaces
    # on the same line (using [ \t] instead of \s to avoid swallowing newlines/bullet dashes)
    cleaned = re.sub(r'(\*\*[^*]+\*\*)(?:[ \t]*[:\-—–·][ \t]*)(?=[ \t]*\w)', r'\1 ', text)
    cleaned = re.sub(r'(\*\*[^*]+\*\*)(?:[ \t]*[:\-—–·][ \t]*)$', r'\1', cleaned)
    return cleaned

def clear_shape_shadow(shape):
    """Remove any shadow effect from a shape's spPr effectLst."""
    try:
        sp = shape._element
        spPr = sp.find(qn("p:spPr"))
        if spPr is None:
            spPr = sp.find(qn("a:spPr"))
        if spPr is None:
            return
        effect_lst = spPr.find(qn("a:effectLst"))
        if effect_lst is not None:
            # Remove outerShdw / innerShdw / glowEffect children
            for tag in ("a:outerShdw", "a:innerShdw", "a:glow", "a:reflection"):
                el = effect_lst.find(qn(tag))
                if el is not None:
                    effect_lst.remove(el)
        # Also clear effectDag if present
        effect_dag = spPr.find(qn("a:effectDag"))
        if effect_dag is not None:
            spPr.remove(effect_dag)
    except Exception as e:
        _log.debug("clear_shadow_failed", shape=getattr(shape, "name", "?"), error=str(e))


def clear_slide_shadows(slide):
    """Remove shadow effects from every shape on a slide."""
    for shape in slide.shapes:
        clear_shape_shadow(shape)


def set_shape_dash_style(shape, style_name: str = "sysDot"):
    """Set preset dash style for shape outline using OpenXML."""
    try:
        spPr = shape._element.spPr
        ln = spPr.find(qn("a:ln"))
        if ln is not None:
            prstDash = ln.find(qn("a:prstDash"))
            if prstDash is not None:
                ln.remove(prstDash)
            new_dash = etree.SubElement(ln, qn("a:prstDash"))
            new_dash.set("val", style_name)
    except Exception as e:
        _log.warning("dash_style_failed", style=style_name, error=str(e))


def _set_panel_border(shape):
    """Set themed dashed border on a panel: tx1 (dark) scheme color + sysDash."""
    try:
        spPr = shape._element.spPr
        ln = spPr.find(qn("a:ln"))
        if ln is not None:
            spPr.remove(ln)
        ln = etree.SubElement(spPr, qn("a:ln"))
        solidFill = etree.SubElement(ln, qn("a:solidFill"))
        schemeClr = etree.SubElement(solidFill, qn("a:schemeClr"))
        schemeClr.set("val", "tx1")
        prstDash = etree.SubElement(ln, qn("a:prstDash"))
        prstDash.set("val", "sysDash")
        if spPr.find(qn("a:effectLst")) is None:
            etree.SubElement(spPr, qn("a:effectLst"))
    except Exception as e:
        _log.warning("panel_border_failed", error=str(e))
def _apply_native_bullet(para, char: str = "•", indent_inches: float = 0.20):
    """Apply native PowerPoint bullet character to a paragraph via OpenXML."""
    try:
        pPr = para._p.get_or_add_pPr()
        # Remove any existing buNone
        for tag in ("a:buNone", "a:buChar", "a:buAutoNum", "a:buFont"):
            el = pPr.find(qn(tag))
            if el is not None:
                pPr.remove(el)
        # Set indent + hanging indent for clean bullet layout
        pPr.set("indent", str(-int(Inches(indent_inches))))
        pPr.set("marL", str(int(Inches(indent_inches))))
        # Bullet font
        buFont = etree.SubElement(pPr, qn("a:buFont"))
        buFont.set("typeface", "Arial")
        buFont.set("charset", "0")
        # Bullet character
        buChar = etree.SubElement(pPr, qn("a:buChar"))
        buChar.set("char", char)
    except Exception as e:
        _log.debug("native_bullet_failed", char=char, error=str(e))


def add_markdown_text(slide, text: str, x: float, y: float, w: float, h: float,
                      font_name: str = "Calibri", font_size: float = 12,
                      default_bold: bool = False, italic: bool = False,
                      color_hex: str = "#111827", align=PP_ALIGN.LEFT,
                      word_wrap: bool = True,
                      native_bullets: bool = False) -> object:
    """
    Parses multi-line text, detects indented bullet list syntax (e.g. starting with '-' or '  -'),
    formats bold runs using markdown '**', and adds paragraphs cleanly to a single textbox.
    """
    txBox = slide.shapes.add_textbox(Inches(x), Inches(y), Inches(w), Inches(h))
    tf = txBox.text_frame
    tf.word_wrap = word_wrap
    tf.margin_left = Inches(0)
    tf.margin_right = Inches(0)
    tf.margin_top = Inches(0)
    tf.margin_bottom = Inches(0)

    if not text:
        return txBox

    # Clean bold separators and split into lines
    text_cleaned = clean_bold_separators(text)
    lines = text_cleaned.split("\n")
    
    first_non_empty = True
    
    for line in lines:
        if not line.strip():
            continue
            
        if first_non_empty:
            p = tf.paragraphs[0]
            first_non_empty = False
        else:
            p = tf.add_paragraph()
            
        p.alignment = align
        
        # Bullet detection
        stripped = line.strip()
        is_sub_bullet = line.startswith("  -") or line.startswith("    -") or line.startswith("  *") or line.startswith("    *")
        is_main_bullet = (line.startswith("-") or line.startswith("*") or line.startswith("•")) and not is_sub_bullet
        
        # Configure native PowerPoint paragraph indentation and spacing
        if is_sub_bullet:
            p.level = 1
            p.space_before = Pt(2)
            p.space_after = Pt(2)
            text_to_parse = stripped
        elif is_main_bullet:
            p.level = 0
            p.space_before = Pt(3)
            p.space_after = Pt(3)
            text_to_parse = stripped
        else:
            p.level = 0
            p.space_before = Pt(6)
            p.space_after = Pt(3)
            text_to_parse = stripped

        # Apply native bullet if requested (every non-empty paragraph gets a bullet dot)
        if native_bullets:
            _apply_native_bullet(p)
            
        # Parse double-asterisk bold runs within this paragraph
        parts = text_to_parse.split("**")
        for idx, part in enumerate(parts):
            if not part:
                continue
            run = p.add_run()
            run.text = part
            run.font.name = font_name
            run.font.size = Pt(font_size)
            run.font.bold = (idx % 2 == 1) or default_bold
            run.font.italic = italic
            run.font.color.rgb = rgb(color_hex)
            
    return txBox


