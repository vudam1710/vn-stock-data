def add_breadcrumb(slide, section: str, topic: str, section_color: str):
    """Breadcrumb at (0.89, 0.41). Section in #94A3B8, ' | Topic' in section_color bold."""
    txBox = slide.shapes.add_textbox(Inches(0.89), Inches(0.41), Inches(8.0), Inches(0.27))
    tf = txBox.text_frame
    tf.word_wrap = False
    p = tf.paragraphs[0]

    r1 = p.add_run()
    r1.text = section + "  |  "
    r1.font.name = "Calibri Light"
    r1.font.size = Pt(10)
    r1.font.bold = False
    r1.font.color.rgb = rgb("#94A3B8")

    r2 = p.add_run()
    r2.text = topic
    r2.font.name = "Calibri"
    r2.font.size = Pt(10)
    r2.font.bold = True
    r2.font.color.rgb = rgb(section_color)


def add_headline(slide, text: str, accent_phrases: list | None = None,
                 accent_color: str = "#111827"):
    """
    Headline at (0.89, 0.68) 11.56×0.77". Calibri 20pt bold #111827.
    accent_phrases: list of substrings to color with accent_color (bold).
    """
    text = clean_bold_separators(text)
    if not accent_phrases:
        add_text(slide, text, 0.89, 0.68, 11.56, 0.77,
                 font_name="Calibri", font_size=20, bold=True, color_hex="#111827", word_wrap=True)
        return

    # Build segments with accent coloring
    remaining = str(text)
    segments: list = []
    for phrase in (accent_phrases or []):
        idx = remaining.find(phrase)
        if idx == -1:
            continue
        if idx > 0:
            segments.append((remaining[:idx], True, "#111827"))
        segments.append((phrase, True, accent_color))
        remaining = remaining[idx + len(phrase):]
    if remaining:
        segments.append((remaining, True, "#111827"))
    if not segments:
        segments = [(text, True, "#111827")]
    add_rich_text(slide, segments, 0.89, 0.68, 11.56, 0.77,
                  font_name="Calibri", font_size=20, word_wrap=True)


def add_slide_number(slide, number: int | str):
    """Slide number at (12.3, 7.10). Calibri 9pt #94A3B8."""
    add_text(slide, str(number), 12.3, 7.10, 0.8, 0.3,
             font_size=9, color_hex="#94A3B8", align=PP_ALIGN.RIGHT)


def _parse_breadcrumb(raw: str) -> tuple[str, str]:
    """Split 'Section  |  Topic' or fallback."""
    for sep in ("  |  ", " | ", "|"):
        if sep in raw:
            parts = raw.split(sep, 1)
            return parts[0].strip(), parts[1].strip()
    return raw.strip(), ""


def _section_accent(slide_data: dict) -> str:
    section = slide_data.get("section", "context").lower()
    return SECTION_ACCENT.get(section, C["blue"])


def _standard_header(slide, slide_data: dict):
    """Apply breadcrumb + headline + slide number (no background rect — relies on master)."""
    accent = _section_accent(slide_data)
    bc_raw = slide_data.get("breadcrumb", "")
    if not bc_raw:
        bs = slide_data.get("breadcrumb_section", "")
        bt = slide_data.get("breadcrumb_topic", "")
        if bs and bt:
            bc_raw = f"{bs}  |  {bt}"
        elif bs or bt:
            bc_raw = bs or bt
    if bc_raw:
        sec, topic = _parse_breadcrumb(bc_raw)
        add_breadcrumb(slide, sec, topic, accent)
    headline = slide_data.get("headline", "")
    # Support both field names: accent_phrases (legacy) and headline_accents (story_arc.json)
    accent_phrases = slide_data.get("accent_phrases") or slide_data.get("headline_accents", [])
    add_headline(slide, headline, accent_phrases=accent_phrases or None,
                 accent_color=accent)
    num = slide_data.get("slide_number", "")
    if num:
        add_slide_number(slide, num)


