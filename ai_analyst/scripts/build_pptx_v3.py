def build_pptx(stem: str, output_path: str | None = None, run_id: str | None = None,
               force: bool = False):
    """
    Read story_arc.json + chart_images.json for *stem*, build slidedeck.pptx.

    Args:
        stem:        Dataset stem, e.g. 'sales_orders_2023_2026'
        output_path: Override output file path (optional)
        run_id:      Pipeline run ID for tracing (auto-generated if None)
    """
    import re
    global _chart_map, _chart_dir

    # CP6 — Sanitize stem: allow only alphanumeric, hyphens, underscores, dots
    if not re.match(r'^[\w\-\.]+$', stem):
        raise ValueError(f"Invalid stem '{stem}': must contain only alphanumeric, hyphens, underscores, dots")

    run_id = run_id or new_run_id()
    log = get_logger(__name__, run_id=run_id, stem=stem)

    pipeline = PIPELINE_DIR(stem)
    arc_path = pipeline / "story_arc.json"

    if not arc_path.exists():
        log.error("story_arc_missing", path=str(arc_path))
        sys.exit(1)

    with open(arc_path, encoding="utf-8") as f:
        arc = json.load(f)

    # CP2 — Validate required story_arc.json fields
    _schema_errors: list[str] = []
    if "slides" not in arc or not isinstance(arc["slides"], list):
        _schema_errors.append("missing or invalid 'slides' key (must be a list)")
    else:
        if len(arc["slides"]) == 0:
            _schema_errors.append("'slides' list is empty")
        for _i, _s in enumerate(arc["slides"]):
            if not isinstance(_s, dict):
                _schema_errors.append(f"slides[{_i}] is not an object")
            elif "layout" not in _s:
                _schema_errors.append(f"slides[{_i}] missing required 'layout' field")
    if _schema_errors:
        for _err in _schema_errors:
            log.error("story_arc_invalid", reason=_err)
        sys.exit(1)

    # Load chart_images.json (optional) and scan chart folders
    _chart_map = {}
    chart_images_path = pipeline / "chart_images.json"
    if chart_images_path.exists():
        with open(chart_images_path, encoding="utf-8") as f:
            ci = json.load(f)
            for c in ci.get("charts", []):
                cid = c.get("chart_id", "")
                fname = c.get("filename", "")
                if cid and fname:
                    _chart_map[cid] = pipeline / "chart_images" / fname
    # Scan all chart image folders as fallback
    for folder_name in ("chart_images", "charts"):
        folder = pipeline / folder_name
        if folder.exists():
            for img in folder.glob("*.png"):
                if img.stem not in _chart_map:
                    _chart_map[img.stem] = img
    _chart_dir = pipeline / "chart_images"

    # Determine output path
    if output_path:
        out = Path(output_path)
    else:
        report_type = arc.get("report_type", arc.get("type", stem.split("_")[0]))
        out_dir = REPORTS_DIR(stem, report_type)
        out_dir.mkdir(parents=True, exist_ok=True)
        out = out_dir / "slidedeck.pptx"
    out.parent.mkdir(parents=True, exist_ok=True)

    # CP3 / CP8 — Idempotency + HITL: skip or confirm before overwriting
    if out.exists():
        if not force:
            log.info("output_exists_skip", path=str(out),
                     hint="pass force=True or --force to overwrite")
            print(f"  Output already exists, skipping: {out}")
            print(f"  Run with --force to overwrite.")
            return str(out)
        # force=True: ask for confirmation in interactive mode (HITL)
        if sys.stdin.isatty():
            answer = input(f"  [HITL] Overwrite existing file? {out} [y/N] ").strip().lower()
            if answer not in ("y", "yes"):
                log.info("output_overwrite_cancelled", path=str(out))
                print("  Cancelled.")
                return str(out)
        log.warning("output_overwrite", path=str(out), forced=True)

    # Initialise presentation from template
    prs, blank_layout = init_presentation()

    slides = arc.get("slides", [])
    log.info("build_start", slide_count=len(slides), output=str(out))

    errors = []
    for i, slide_data in enumerate(slides):
        slide_data["_stem"] = stem
        layout = slide_data.get("layout") or slide_data.get("type") or _FALLBACK_LAYOUT
        layout = layout.upper().replace(" ", "_")

        builder = LAYOUT_BUILDERS.get(layout)
        if builder is None:
            log.warning("unknown_layout", slide=i+1, layout=layout, fallback=_FALLBACK_LAYOUT)
            builder = LAYOUT_BUILDERS[_FALLBACK_LAYOUT]

        num = slide_data.get("slide_number", i + 1)
        headline = slide_data.get("headline", "")[:70]
        # Keep stdout for human-readable progress (consistent with existing UX)
        print(f"  [{num}] {layout:<22} {headline}")
        log.debug("slide_build", slide=num, layout=layout)

        try:
            built_slide = builder(prs, blank_layout, slide_data)
            if built_slide is not None:
                clear_slide_shadows(built_slide)
        except Exception as exc:
            log.error("slide_render_error", slide=num, layout=layout, error=str(exc))
            traceback.print_exc()
            errors.append({"slide": num, "layout": layout, "error": str(exc)})
            try:
                err_slide = add_slide(prs, blank_layout)
                add_bg(err_slide)
                add_text(err_slide,
                         f"[Slide {num} — render error: {layout}]",
                         0.89, 0.68, 11.56, 0.77,
                         font_size=20, bold=True, color_hex=C["red"])
            except Exception as e2:
                log.debug("error_slide_failed", slide=num, error=str(e2))

    prs.save(str(out))
    size_kb = os.path.getsize(str(out)) / 1024
    log.info("build_complete", output=str(out), size_kb=round(size_kb, 1),
             slide_count=len(slides), error_count=len(errors))
    print(f"Saved: {out} ({size_kb:.1f} KB)")
    return str(out)


