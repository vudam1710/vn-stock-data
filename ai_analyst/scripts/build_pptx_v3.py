_chart_map: dict = {}
_chart_dir: Path = Path(".")

def _resolve_chart(chart_id: str | None) -> Path | None:
    if not chart_id:
        return None
    rel = _chart_map.get(chart_id)
    if rel:
        p = BASE / rel
        if p.exists():
            return p
    # Fallback: look in chart_images folder
    fallback = _chart_dir / f"{chart_id}.png"
    if fallback.exists():
        return fallback
    return None


