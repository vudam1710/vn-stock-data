def init_presentation() -> tuple[Presentation, object]:
    """Load template, strip all existing slides, return (prs, BLANK_LAYOUT)."""
    prs = Presentation(str(TEMPLATE_PATH))
    # Remove all existing template slides safely via XML
    sldIdLst = prs.slides._sldIdLst
    for sld_id in list(sldIdLst.sldId_lst):
        rId = sld_id.get('{http://schemas.openxmlformats.org/officeDocument/2006/relationships}id')
        if rId:
            try:
                prs.part.drop_rel(rId)
            except Exception as e:
                _log.debug("drop_rel_failed", rId=rId, error=str(e))
        sldIdLst.remove(sld_id)
    BLANK_LAYOUT = prs.slide_layouts[6]
    return prs, BLANK_LAYOUT


